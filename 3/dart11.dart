void main() {

  int selectorOperator = 1;

  switch(selectorOperator){

    case 1 :
      print('Opcion 1');
    break;

    case 2:
      print('Opcion 2');
    break;

    case 3:
    print('Opcion 3');
      break;

    default:
      print('No eligio una opcion correcta');

  }
}
